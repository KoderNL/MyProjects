public class Employee {
    long id;
    String name;
    int age;
    int salary;
    char gender;
    public Employee(long id, String name, int age, int salary, char gender) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.salary = salary;
        this.gender = gender;
    }
}
